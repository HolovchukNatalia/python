# Спортмен у перший день подолав 10 км дистанції. 
# Кожного наступного дня він пробігав на 10% більше, ніж попереднього. 
# За скільки днів спортсмен подолає 50 км?

distance = 10
total = 0
day = 0

while total < 50:
	total += distance
	distance *= 1.1
	day += 1

print("Спортсмен подолає 50 км за", day, "днів")