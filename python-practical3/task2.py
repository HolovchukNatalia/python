# Підрахувати, скільки разів цифра 7 зустрічається в десятковому запису
# заданого чотиризначного числа n.

n = input("Введіть чотиризначне натуральне число: ")

if len(n) != 4 or not n.isdigit():
    print("Потрібно ввести чотиризначне число!")
else:
    count = n.count('7')
    print("Кількість цифр 7 в числі:", count)
